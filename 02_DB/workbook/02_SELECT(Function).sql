-- Additional SELECT 함수

-- 1번
SELECT STUDENT_NO "학번", STUDENT_NAME "이름", TO_CHAR(ENTRANCE_DATE, 'YYYY-MM-DD') "입학년도"
FROM TB_STUDENT
WHERE DEPARTMENT_NO = '002'
ORDER BY ENTRANCE_DATE ;

-- 2번
SELECT PROFESSOR_NAME, PROFESSOR_SSN
FROM TB_PROFESSOR
WHERE PROFESSOR_NAME NOT LIKE '___';

-- 3번 
SELECT PROFESSOR_NAME "교수이름", 
	FLOOR(MONTHS_BETWEEN(SYSDATE, TO_DATE('19' || 
	SUBSTR(PROFESSOR_SSN, 1, 6), 'YYYYMMDD')) / 12 ) 나이
FROM TB_PROFESSOR
WHERE SUBSTR(PROFESSOR_SSN,8,1) = '1'
ORDER BY 나이;
-- 나이 -n살 나온 이유: 50미만 숫자는 2000년대로 반영(RR,YY)

-- 4번
SELECT SUBSTR(PROFESSOR_NAME, 2) "이름"
FROM TB_PROFESSOR;

-- 5번 
SELECT STUDENT_NO, STUDENT_NAME
FROM TB_STUDENT
WHERE EXTRACT(YEAR FROM ENTRANCE_DATE)- EXTRACT(YEAR FROM(TO_DATE(SUBSTR(STUDENT_SSN,1,6)))) >= 20;

-- 6번 
SELECT TO_CHAR(TO_DATE('201225'), 'DAY') "2020년 크리스마스 요일은?"
FROM DUAL;

